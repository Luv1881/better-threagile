// Package report provides report rendering functions.
// This file adds a Markdown export (H.2) for CI-friendly threat model reports.
package report

import (
	"fmt"
	"sort"
	"strings"
	"time"

	"github.com/threagile/threagile/pkg/types"
)

// MarkdownReport generates a GitHub-Flavored Markdown threat model report.
// The output is suitable for committing to the repository and rendering inline in PR reviews.
func MarkdownReport(model *types.Model) string {
	var b strings.Builder

	b.WriteString("# Threat Model Report\n\n")
	b.WriteString(fmt.Sprintf("**Model:** %s  \n", model.Title))
	b.WriteString(fmt.Sprintf("**Generated:** %s  \n", time.Now().UTC().Format("2006-01-02 15:04 UTC")))
	if model.Author != nil && model.Author.Name != "" {
		b.WriteString(fmt.Sprintf("**Author:** %s  \n", model.Author.Name))
	}
	b.WriteString("\n---\n\n")

	// Summary table
	risks := collectRisks(model)
	if len(risks) > 0 {
		byCritical := countBySeverity(risks, types.CriticalSeverity)
		byHigh := countBySeverity(risks, types.HighSeverity)
		byMedium := countBySeverity(risks, types.MediumSeverity)
		byLow := countBySeverity(risks, types.LowSeverity)
		byElevated := countBySeverity(risks, types.ElevatedSeverity)

		b.WriteString("## Summary\n\n")
		b.WriteString("| Severity | Count |\n|---|---|\n")
		if byCritical > 0 {
			b.WriteString(fmt.Sprintf("| 🔴 Critical | %d |\n", byCritical))
		}
		if byHigh > 0 {
			b.WriteString(fmt.Sprintf("| 🟠 High | %d |\n", byHigh))
		}
		if byElevated > 0 {
			b.WriteString(fmt.Sprintf("| 🟡 Elevated | %d |\n", byElevated))
		}
		if byMedium > 0 {
			b.WriteString(fmt.Sprintf("| 🔵 Medium | %d |\n", byMedium))
		}
		if byLow > 0 {
			b.WriteString(fmt.Sprintf("| ⚪ Low | %d |\n", byLow))
		}
		b.WriteString(fmt.Sprintf("| **Total** | **%d** |\n\n", len(risks)))
	}

	// Findings by severity
	b.WriteString("## Findings\n\n")
	if len(risks) == 0 {
		b.WriteString("*No findings generated.*\n\n")
	} else {
		sortedRisks := sortRisksBySeverity(risks)
		for _, r := range sortedRisks {
			sev := strings.ToUpper(r.Severity.String())
			b.WriteString(fmt.Sprintf("### [%s] %s\n\n", sev, stripHTML(r.Title)))
			b.WriteString(fmt.Sprintf("- **ID:** `%s`\n", r.SyntheticId))
			b.WriteString(fmt.Sprintf("- **Category:** `%s`\n", r.CategoryId))
			b.WriteString(fmt.Sprintf("- **Impact:** %s\n", r.ExploitationImpact.String()))
			b.WriteString(fmt.Sprintf("- **Likelihood:** %s\n", r.ExploitationLikelihood.String()))
			if r.MostRelevantTechnicalAssetId != "" {
				if asset, ok := model.TechnicalAssets[r.MostRelevantTechnicalAssetId]; ok {
					b.WriteString(fmt.Sprintf("- **Asset:** %s\n", asset.Title))
				}
			}
			if len(r.DataBreachTechnicalAssetIDs) > 0 {
				assetNames := make([]string, 0, len(r.DataBreachTechnicalAssetIDs))
				for _, id := range r.DataBreachTechnicalAssetIDs {
					if a, ok := model.TechnicalAssets[id]; ok {
						assetNames = append(assetNames, a.Title)
					}
				}
				if len(assetNames) > 0 {
					b.WriteString(fmt.Sprintf("- **Data Breach Scope:** %s\n", strings.Join(assetNames, ", ")))
				}
			}
			b.WriteString("\n")
		}
	}

	// Technical assets table
	b.WriteString("## Technical Assets\n\n")
	if len(model.TechnicalAssets) == 0 {
		b.WriteString("*No technical assets defined.*\n\n")
	} else {
		b.WriteString("| Asset | Type | Internet | Confidentiality | Integrity | Availability |\n")
		b.WriteString("|---|---|---|---|---|---|\n")
		assetList := sortedAssets(model)
		for _, a := range assetList {
			internet := "No"
			if a.Internet {
				internet = "**Yes**"
			}
			b.WriteString(fmt.Sprintf("| %s | %s | %s | %s | %s | %s |\n",
				a.Title, a.Type.String(), internet,
				a.Confidentiality.String(), a.Integrity.String(), a.Availability.String()))
		}
		b.WriteString("\n")
	}

	// Data assets table
	if len(model.DataAssets) > 0 {
		b.WriteString("## Data Assets\n\n")
		b.WriteString("| Asset | Confidentiality | PII |\n|---|---|---|\n")
		for _, da := range model.DataAssets {
			pii := "No"
			if da.HasPii || len(da.PiiCategories) > 0 {
				pii = "**Yes**"
			}
			b.WriteString(fmt.Sprintf("| %s | %s | %s |\n", da.Title, da.Confidentiality.String(), pii))
		}
		b.WriteString("\n")
	}

	b.WriteString("---\n")
	b.WriteString("*Generated by [threagile](https://github.com/threagile/threagile)*\n")
	return b.String()
}

func collectRisks(model *types.Model) []*types.Risk {
	var risks []*types.Risk
	for _, r := range model.GeneratedRisksBySyntheticId {
		risks = append(risks, r)
	}
	return risks
}

func countBySeverity(risks []*types.Risk, sev types.RiskSeverity) int {
	n := 0
	for _, r := range risks {
		if r.Severity == sev {
			n++
		}
	}
	return n
}

func sortRisksBySeverity(risks []*types.Risk) []*types.Risk {
	out := make([]*types.Risk, len(risks))
	copy(out, risks)
	sort.Slice(out, func(i, j int) bool {
		if out[i].Severity != out[j].Severity {
			return out[i].Severity > out[j].Severity
		}
		return out[i].SyntheticId < out[j].SyntheticId
	})
	return out
}

func sortedAssets(model *types.Model) []*types.TechnicalAsset {
	out := make([]*types.TechnicalAsset, 0, len(model.TechnicalAssets))
	for _, a := range model.TechnicalAssets {
		out = append(out, a)
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Title < out[j].Title })
	return out
}

// stripHTML removes simple HTML tags like <b> and </b> for Markdown output.
func stripHTML(s string) string {
	var result strings.Builder
	inTag := false
	for _, ch := range s {
		if ch == '<' {
			inTag = true
			continue
		}
		if ch == '>' {
			inTag = false
			continue
		}
		if !inTag {
			result.WriteRune(ch)
		}
	}
	return result.String()
}
